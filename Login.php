        async function Login() {

            try {


                var email = document.getElementById("loginEmail").value;


                if (email == "") {
                    Swal.fire({
                        title: 'Error!',
                        text: 'Email is required',
                        icon: 'error',
                        timer: 2000,
                        timerProgressBar: true,
                        showConfirmButton: false
                    });
                    return;
                }else if (!(/^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/.test(email))) {
                    Swal.fire({
                        title: 'Error!',
                        text: 'Invalid Email',
                        icon: 'error',
                        timer: 2000,
                        timerProgressBar: true,
                        showConfirmButton: false
                    });
                    return;
                }

                var password = document.getElementById("loginPassword").value;


                if (password == "") {
                    Swal.fire({
                        title: 'Error!',
                        text: 'Password is required',
                        icon: 'error',
                        timer: 2000,
                        timerProgressBar: true,
                        showConfirmButton: false
                    });
                    return;
                }


                var dataStringr = 'email_id=' + email + '&password=' + password;


                console.log(dataStringr);

                $.ajax({
                    type: "POST",
                    url: "WalletLogin2",
                    data: dataStringr,
                    cache: false,
                    success: function(status) {
                        if (status == "Exist") {
                            Swal.fire({
                                title: 'Success!',
                                text: 'Login successful',
                                icon: 'success',
                                timer: 2000,
                                timerProgressBar: true,
                                showConfirmButton: false
                            }).then(() => {
                                window.location.href = 'binary-tree';
                            });
                        } else {
                            Swal.fire({
                                title: 'Error!',
                                text: 'Email or Password is incorrect',
                                icon: 'error',
                                timer: 2000,
                                timerProgressBar: true,
                                showConfirmButton: false
                            });
                        }
                    },
                    error: function(xhr, status, error) {
                        Swal.fire({
                            title: 'Error!',
                            text: 'An error occurred during login',
                            icon: 'error',
                            timer: 2000,
                            timerProgressBar: true,
                            showConfirmButton: false
                        });
                    }
                });

            } catch (error) {
                if (error.code === 4001) {
                    Swal.fire({
                        title: 'Error!',
                        text: 'User denied account access',
                        icon: 'error',
                        timer: 2000,
                        timerProgressBar: true,
                        showConfirmButton: false
                    });
                } else {
                    console.log(error);
                }
            }
        }
    </script>

